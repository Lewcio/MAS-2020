import Foundation
import UIKit

extension UIColor {
    class var redBackground: UIColor {
        return UIColor(red: 0.86, green: 0.15, blue: 0.15, alpha: 1)
    }
    
    class var redMain: UIColor {
        return UIColor(red: 0.57, green: 0, blue: 0, alpha: 1)
    }
    
    class var redDark: UIColor {
        return UIColor(red: 0.4, green: 0, blue: 0, alpha: 1)
    }
    
    
    class var blueBackground: UIColor {
        return UIColor(red: 0.3, green: 0.62, blue: 0.69, alpha: 1)
    }
    
    class var blueMain: UIColor {
        return UIColor(red: 0.05, green: 0.49, blue: 0.59, alpha: 1)
    }
    
    class var blueDark: UIColor {
        return UIColor(red: 0.01, green: 0.37, blue: 0.46, alpha: 1)
    }
}
